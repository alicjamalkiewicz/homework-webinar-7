package pl.test.forLoop;

public class foorLoopHomework {
    public static void main(String[] args) {
        printEachNthNumberBelow(3,5);
    }

    public static void printEachNthNumberBelow(int n, int limit) {
        for (n = 0; n < 3; n++){
            System.out.println(limit);
        }
    }
}
